const express = require('express');
const app = express();
app.use(express.json());
const ActorControllerController = require('./ActorControllerController.js');
app.use('/actorcontrollercontroller', ActorControllerController);
const CategoryControllerController = require('./CategoryControllerController.js');
app.use('/categorycontrollercontroller', CategoryControllerController);
const CustomerControllerController = require('./CustomerControllerController.js');
app.use('/customercontrollercontroller', CustomerControllerController);
const FilmControllerController = require('./FilmControllerController.js');
app.use('/filmcontrollercontroller', FilmControllerController);
const MainControllerController = require('./MainControllerController.js');
app.use('/maincontrollercontroller', MainControllerController);
const StaffControllerController = require('./StaffControllerController.js');
app.use('/staffcontrollercontroller', StaffControllerController);
app.get('/health', (req, res) => res.json({status:'ok'}));
const port = process.env.PORT || 3000;
app.listen(port, () => console.log(`Server listening on ${port}`));